                <h4>Los tejidos</h4>
                <p>
	                Lana<br>
					Encoge si se lava a temperatura muy alta y se deforma si se seca colgada. Es un buen aislante térmico. Tiende a apelmazarse y a amarillear.<br><br>
					Algodón<br>
					Encoge si se lava a demasiada temperatura. Resiste bien la plancha y, si tiene color, debe lavarse por separado la primera vez. Es fresco, flexible y de alta resistencia. Tiende a arrugarse.<br><br>
					Lino<br>
					Debe lavarse siempre a mano, a bajas temperaturas y sin centrifugarse. Resiste bien la plancha. Es fresco y agradable al tacto y resiste bien la suciedad.<br><br>
					Seda<br>
					No admite temperaturas muy altas ni centrifugado. Debe plancharse sin vapor y a baja temperatura. Es fuerte, ligero, de tacto agradable y fija bien los colores. Es delicado. Con manchas rebeldes requiere la atención de un profesional.<br><br>
					Acetato, rayón o viscosa<br>
					No deben centrifugarse para evitar deformaciones. Tienen tacto sedoso y fijan bien los colores. Tienen baja resistencia en estado húmedo, arden con facilidad y se cargan de electricidad estática.<br><br>
					Poliéster o nylon<br>
					Son sensibles al calor, deben ser lavados en agua tibia y planchados a baja temperatura sin vapor. Son muy resistentes, se arrugan poco y fijan bien los colores. Arden con facilidad y se cargan de electricidad estática.
                </p>
