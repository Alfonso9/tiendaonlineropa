			<div id="GroupESub1" class="subgroup">
                <h4>Consejos Prácticos</h4>
                <p>
                ¿Debo lavar una prenda?<br>
				Hay veces que con ventilar y cepillar las prendas es suficiente si no están sucias. Si lo haces, ahorrarás energía y tu ropa durará más.<br><br>
				Detergente<br>
				Si utilizas un detergente con etiquetas ecológicas, ayudarás al medio ambiente. Intenta no utilizar más detergente que el que aparece en las instrucciones del envase. Utilizar cantidades superiores puede provocar manchas en la ropa y causar problemas cutáneos.<br><br>
				Suavizante<br>
				El suavizante es un gasto innecesario y resulta altamente dañino para el medioambiente.<br><br>
				Lejía<br>
				Utiliza la lejía sólo en agua fría y una hora como máximo. No emplees recipientes de hierro, cobre o latón.<br><br>
				Lavado a mano<br>
				Acuérdate de disolver bien el detergente antes de introducir la ropa. En los primeros lavados, separa la prenda, ya que el agua puede colorearse.<br><br>
				La lavadora<br>
				Utiliza la lavadora a plena capacidad. Para lavar pocas prendas, es recomendable utilizar un programa de bajo consumo energético, opción existente en la mayoría de lavadoras modernas. Acuérdate de vaciar los bolsillos.<br><br>
				Temperatura de lavado<br>
				Respeta siempre la temperatura de lavado indicada. Las prendas de color que deban ser lavadas a 60 grados pueden desteñir si se lavan a 40 grados o a mano. No siempre es mejor lavar a menor temperatura.<br><br>
				Secado<br>
				Es aconsejable centrifugar bien la ropa antes de secarla. Acuérdate que el aire libre es la mejor secadora, aunque evita exponer las prendas directamente al sol, para evitar que pierdan color.<br><br>
				Plancha<br>
				Clasifica tus prendas según el grado de temperatura de la etiqueta y empieza por la más baja. Es mejor que la ropa esté algo húmeda para plancharla. Al finalizar, y antes de que se enfríe la plancha, elimina con un trapo húmedo cualquier resto que se haya quedado pegado.<br><br>
				Recicla tu ropa<br>
				Las prendas que ya no uses, dónalas a organizaciones que las distribuyen a personas necesitadas. Así, prolongarás la vida útil de tu ropa.<br><br>
				Denim<br>
				El denim debe ese aspecto a una técnica de coloración que, al lavarlo, pueda desprender pequeños pigmentos de color que destiñen. Lava los vaqueros del revés para que no pierdan color y evitar la formación de marcas blancas en las arrugas. <br><br>
				Plumones<br>
				Mete pelotas de tenis en la secadora para conseguir una distribución uniforme del relleno. <br><br>
				Prendas de punto grueso<br>
				Las prendas de punto grueso han de secarse envueltas en una toalla y en posición horizontal, para que conserven su forma original. <br><br>
				Ropa impermeable<br>
				Para secar la ropa impermeable sólo hay que dejarla escurrir. Si prefieres meterla en la secadora, recuerda que ha de ser siempre a temperaturas muy bajas.
                </p>
